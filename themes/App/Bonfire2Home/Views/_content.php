    <x-content-card id="philosophy" title="Philosophy">
        <p class="card-text">We believe that the best tools don't get in your way. They don't fight you, or make you jump through hoops to get your work done. But they also don't do everything for you. They provide a solid foundation, and then get out of the way.</p>
        <p class="card-text">We believe that a product can only be as easy to use as its documentation. If you can't figure out how to use it, then it's not a good tool.</p>
        <p class="card-text">We also know that we don't have all the answers. We don't know your specific needs, or the best way to solve your problems. Therefore the tool should be flexible enough to allow you to solve your problems in the way that makes the most sense to you.</p>
        <p class="card-text">We believe we're going to get it wrong along the way. Paths that we're sure are the right ones will turn out to be dead ends. We're going to need to change direction. But when we do it should be in a way that doesn't break your work.</p>
        <p class="card-text">We believe that the best tools are not created in a silo. They are created with the input and feedback of the people that use them. The community is a vital part of the process, from writing documentation, to testing, to contributing code and bug fixes. We are in this together.</p>
